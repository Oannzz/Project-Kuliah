    public void tambahPengaduanKeInstansi(String pengaduan, String lokasi, String tanggal, String klasifikasi, String kodePengaduan) {

        DefaultTableModel model = (DefaultTableModel) tbl_pengaduan.getModel();

        // Cek apakah kode pengaduan sudah ada di tabel agar tidak duplikat
        if (model.getColumnCount() != 5) {
            loadPengaduanTerbaru(); // inisialisasi model kosong dulu
            model = (DefaultTableModel) tbl_pengaduan.getModel();
        }

        // Cek duplikat
        for (int i = 0; i < model.getRowCount(); i++) {
            if (model.getValueAt(i, 4) != null
                    && model.getValueAt(i, 4).toString().equals(kodePengaduan)) {
                return;
            }
        }

        model.addRow(new Object[]{
            pengaduan,
            lokasi,
            tanggal,
            klasifikasi,
            kodePengaduan
        });

        // Pasang renderer dan editor ulang supaya tombol icon search tetap muncul
        setupTombolDetail();
    }

    private void setupTombolDetail() {
        tbl_pengaduan.getColumnModel().getColumn(4).setCellRenderer(new DasborInstansi1.KodeWithButtonRenderer());
        tbl_pengaduan.getColumnModel().getColumn(4).setCellEditor(new DasborInstansi1.KodeWithButtonEditor());
        tbl_pengaduan.getColumnModel().getColumn(4).setPreferredWidth(220);
        tbl_pengaduan.getColumnModel().getColumn(4).setMinWidth(200);
        tbl_pengaduan.setRowHeight(28);
    }

    private void bukaDetailPengaduan(int row) {
        String kodePengaduan = tbl_pengaduan.getValueAt(row, 4).toString(); // ← index 4

        try (Connection conn = java.sql.DriverManager.getConnection(
                "jdbc:mysql://127.0.0.1:3306/silandak", "root", "")) {
            String sql = "SELECT * FROM form_pengaduan WHERE kode_pengaduan = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, kodePengaduan);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                DetailPengaduanTerbaruInstansi detail = new DetailPengaduanTerbaruInstansi(
                        rs.getString("kode_pengaduan"),
                        rs.getString("deskripsi"),
                        rs.getString("alamat_kejadian"),
                        rs.getTimestamp("waktu_kejadian").toString(),
                        this.idInstansiDinamis,
                        this.namaInstansiData, // ✅ teruskan
                        this.shiftInstansiData
                );
                detail.setVisible(true);
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal membuka detail: " + e.getMessage());
        }
    }


    private void cariPengaduan(String kode, DefaultTableModel model) {
        model.setRowCount(0);
        try (Connection conn = java.sql.DriverManager.getConnection(
                "jdbc:mysql://127.0.0.1:3306/silandak", "root", "")) {
            String sql = "SELECT deskripsi, alamat_kejadian, waktu_kejadian, klasifikasi, kode_pengaduan, status "
                    + "FROM form_pengaduan WHERE kode_pengaduan = ? ORDER BY created_at DESC";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, kode);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String klas = rs.getString("klasifikasi").equals("emergency") ? "E" : "N.e";
                String status = switch (rs.getString("status")) {
                    case "masuk_sistem" ->
                        "Masuk Sistem";
                    case "diproses" ->
                        "Diproses";
                    case "ditangani" ->
                        "Ditangani";
                    case "selesai" ->
                        "Selesai";
                    case "ditolak" ->
                        "Ditolak";
                    default ->
                        rs.getString("status");
                };
                if (model.getColumnCount() == 5) {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        rs.getString("alamat_kejadian"),
                        rs.getString("waktu_kejadian"),
                        klas,
                        rs.getString("kode_pengaduan")
                    });
                } else if (model.getColumnCount() == 3 && model.getColumnName(1).equals("Klasifikasi")) {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        klas,
                        rs.getString("kode_pengaduan")
                    });
                } else {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        status,
                        rs.getString("kode_pengaduan")
                    });
                }
            }
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Kode pengaduan tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

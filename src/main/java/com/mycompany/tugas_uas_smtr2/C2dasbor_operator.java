    private void loadRiwayatPengaduan() {
    DefaultTableModel model = (DefaultTableModel) tbl_rwyt.getModel();
    model.setRowCount(0);
    Connection con = dbconnectionsistem.getKoneksi();
    if (con == null) return;

    String sql = "SELECT fp.deskripsi, fp.klasifikasi, fp.kode_pengaduan " +
                 "FROM form_pengaduan fp " +
                 "INNER JOIN laporan_penanganan lp ON fp.id_form_pengaduan = lp.id_form_pengaduan " +
                 "ORDER BY lp.created_at DESC";
    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            String klas = rs.getString("klasifikasi").equals("emergency") ? "E" : "N.e";
            model.addRow(new Object[]{
                rs.getString("deskripsi"),
                klas,
                rs.getString("kode_pengaduan")
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error load riwayat: " + e.getMessage());
    }
}


    private void loadDataPengaduanTerbaru() {
        DefaultTableModel model = new DefaultTableModel(
                new Object[]{"Pengaduan", "Lokasi", "Tanggal", "Kode Pengaduan"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return column == 3;
            }
        };

        try {
            Connection conn = dbconnectionsistem.getKoneksi();
            String sql = "SELECT deskripsi, alamat_kejadian, waktu_kejadian, kode_pengaduan "
                    + "FROM form_pengaduan ORDER BY waktu_kejadian DESC";
            PreparedStatement ps = conn.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("deskripsi"),
                    rs.getString("alamat_kejadian"),
                    rs.getTimestamp("waktu_kejadian").toString(),
                    rs.getString("kode_pengaduan")
                });
            }
            rs.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }

        tbl_trbr.setModel(model);
        setupTombolDetail();
    }


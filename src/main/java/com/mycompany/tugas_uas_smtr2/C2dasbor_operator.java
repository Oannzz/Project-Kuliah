    private void bukaDetailLaporan(String kodePengaduan) {
  
    Connection con = dbconnectionsistem.getKoneksi();
    if (con == null) return;

    String sql = "SELECT fp.kode_pengaduan, fp.deskripsi, fp.alamat_kejadian, " +
                 "fp.waktu_kejadian, fp.latitude, fp.longitude, " +
                 "lp.deskripsi_penanganan, lp.tanggal_laporan, " +
                 "i.nama_instansi, i.shift " +
                 "FROM form_pengaduan fp " +
                 "INNER JOIN laporan_penanganan lp ON fp.id_form_pengaduan = lp.id_form_pengaduan " +
                 "LEFT JOIN instansi i ON lp.id_instansi = i.id_instansi " +
                 "WHERE fp.kode_pengaduan = ?";
    try {
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, kodePengaduan);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) {
            double lat = rs.getDouble("latitude");
            double lng = rs.getDouble("longitude");
            String lokasiMaps = lat + "," + lng;

            DetailLaporanHasilPenangananOperator detail = new DetailLaporanHasilPenangananOperator(
                rs.getString("kode_pengaduan"),
                rs.getString("deskripsi_penanganan"),
                rs.getString("alamat_kejadian"),
                rs.getString("tanggal_laporan"),
                rs.getString("nama_instansi"),
                rs.getString("shift"),
                lokasiMaps
            );
            detail.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Laporan belum tersedia untuk pengaduan ini");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }

}


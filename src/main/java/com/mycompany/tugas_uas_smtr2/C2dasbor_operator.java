    private void cariPengaduan(String kode, DefaultTableModel model) {
        model.setRowCount(0);
        Connection con = dbconnectionsistem.getKoneksi();
        if (con == null) {
            return;
        }

        String sql = "SELECT deskripsi, alamat_kejadian, waktu_kejadian, klasifikasi, kode_pengaduan, status "
                + "FROM form_pengaduan "
                + "WHERE kode_pengaduan = ? "
                + "ORDER BY created_at DESC";
        try {
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, kode);
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                String klas = rs.getString("klasifikasi").equals("emergency") ? "E" : "N.e";
                String status = switch (rs.getString("status")) {
                    case "masuk_sistem" ->
                        "Masuk Sistem";
                    case "diproses" ->
                        "Diproses";
                    case "ditangani" ->
                        "Ditangani";
                    case "selesai" ->
                        "Selesai";
                    case "ditolak" ->
                        "Ditolak";
                    default ->
                        rs.getString("status");
                };
                if (model.getColumnCount() == 4) {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        rs.getString("alamat_kejadian"),
                        rs.getString("waktu_kejadian"),
                        rs.getString("kode_pengaduan")
                    });
                } else if (model.getColumnCount() == 3 && model.getColumnName(1).equals("Klasifikasi")) {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        klas,
                        rs.getString("kode_pengaduan")
                    });
                } else {
                    model.addRow(new Object[]{
                        rs.getString("deskripsi"),
                        status,
                        rs.getString("kode_pengaduan")
                    });
                }
            }
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(null, "Kode pengaduan tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }

